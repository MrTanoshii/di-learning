for i in range(3):
    spaces = " " * (2 - i)
    stars = "*" * (2 * i + 1)
    print(spaces + stars)

for i in range(5):
    spaces = " " * (4 - i)
    stars = "*" * (i + 1)
    print(spaces + stars)

for i in range(5):
    stars = "*" * (i + 1)
    print(stars)

for i in range(4):
    stars = "*" * (4 - i)
    print(stars)
